-- ============================================================================
-- プロテクトルールに「適用ロール」を追加 (2026-06)
--
-- 各ルールがどのロールのユーザーの対応歴に適用されるかを指定する。
--   - apply_roles が NULL または空配列 → すべてのロールに適用(後方互換)
--   - 非空 → 列挙したロールのユーザーが対応歴を残した時のみプロテクト適用
--
-- 要件: 現行の2ルール(通電/接触対応)は sales のみ適用。
--       他ロールが更新してもプロテクトは反映しない。
-- ============================================================================

ALTER TABLE public.flow_rules
  ADD COLUMN IF NOT EXISTS apply_roles text[];

-- 通電プロテクト / 接触対応プロテクト を sales 限定に
UPDATE public.flow_rules SET apply_roles = ARRAY['sales']::text[]
 WHERE trigger_flag IN ('通電', '接触対応');
